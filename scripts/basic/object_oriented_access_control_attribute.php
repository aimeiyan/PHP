<p>PHP 对属性或方法的访问控制，是通过在前面添加关键字 public（公有），protected（受保护）或 private（私有）来实现的。</p>
<p>public（公有）：公有的类成员可以在任何地方被访问。</p>
<p>protected（受保护）：受保护的类成员则可以被其自身以及其子类和父类访问。</p>
<p>private（私有）：私有的类成员则只能被其定义所在的类访问。</p>

